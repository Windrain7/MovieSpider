class MoviespiderPipeline(object):
    def __init__(self):
        self.f = open('./DouBanMovieInfo.txt', 'w')

    def open_spider(self, spider):
        pass

    def close_spider(self, spider):
        self.f.close()

    def process_item(self, item, spider):
        try:
            line = str(dict(item)) + '\n'
            self.f.write(line)
        except:
            pass
        return item
